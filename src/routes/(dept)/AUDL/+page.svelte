<h1>Audiology</h1>
<CourseCard id="AUD236" shortName="AUD 236" longName="Preceptorship in Neuro-Otology" units="2" url="/AUDL#AUD236">
	<p>Observations in UC San Diego Otology clinic. Learn about procedures for taking histories and performing clinical examinations, patient treatment, and patient education from otologists.</p>

	<p>Prerequisites: second-year AuD student or consent of instructor.</p>
</CourseCard>
<CourseCard id="AUD255" shortName="AUD 255" longName="Anatomy and Physiology of the Auditory and Vestibular Systems" units="4" url="/AUDL#AUD255">
	<p>Detailed anatomy of the auditory and vestibular systems, including the ultra-structure, histology, and neuronal connections of the systems. In addition, physiological responses that underlie the actions of each system are described, from the cellular to the systems levels.</p>

	<p>Prerequisites: second-year AuD student or consent of instructor.</p>
</CourseCard>
<CourseCard id="AUD257" shortName="AUD 257" longName="Ear Diseases and Treatment" units="3" url="/AUDL#AUD257">
	<p>Differential diagnosis and treatment of auditory and vestibular disorders, audiological components of neuro-otology, as well as interactions between the audiologist and neuro-otologist in a clinical setting.</p>

	<p>Prerequisites: second-year AuD student or consent of instructor.</p>
</CourseCard>
<CourseCard id="AUD263" shortName="AUD 263" longName="Physiological and Behavioral Tests of Vestibular Function" units="4" url="/AUDL#AUD263">
	<p>Clinical evaluation of vestibular function, using techniques such as caloric and rotational electronystagmography and posturography. The interpretation of clinical findings and implications for rehabilitative strategies will be covered. The course will include observation of testing in otology clinic settings.</p>

	<p>Prerequisites: second-year AuD student or consent of instructor.</p>
</CourseCard>
<CourseCard id="AUD265" shortName="AUD 265" longName="Aural Rehabilitation" units="3" url="/AUDL#AUD265">
	<p>Consequences of hearing loss on learning and communication and its impact on psychosocial function, vocational options, and quality of life. Emphasis on the impact audiologists can have on the individual by managing people with hearing loss.</p>

	<p>Prerequisites: second- or third-year AuD student or consent of instructor.</p>
</CourseCard>
<CourseCard id="AUD271" shortName="AUD 271" longName="Temporal Bone Anatomy and Neuroanatomy" units="3" url="/AUDL#AUD271">
	<p>Detailed anatomy of the temporal bone, cranial nerves and basic neurophysiology for audiologists. Radiographic and magnetic resonance imaging of the temporal bone will be discussed.</p>

	<p>Prerequisites: second- or third-year AuD student or consent of instructor.</p>
</CourseCard>
<CourseCard id="AUD272" shortName="AUD 272" longName="Central Auditory Processing Assessment and Management" units="4" url="/AUDL#AUD272">
	<p>Central auditory processing assessment and management for pediatrics and adults. Focus on behavioral tests assessing dichotic listening, temporal processes, pattern recognition, performance with competing and degraded signals.</p>

	<p>Prerequisites: course restricted to AuD students.</p>
</CourseCard>
<CourseCard id="AUD274" shortName="AUD 274" longName="Advanced Technical Aspects of Hearing Amplification" units="4" url="/AUDL#AUD274">
	<p>Advanced technical aspects of assessing and fitting amplification devices for the hearing impaired. Intensive hands-on training to develop skills necessary for audiology clinical placement.</p>

	<p>Prerequisites: course restricted to AuD students or consent of instructor.</p>
</CourseCard>
<CourseCard id="AUD275" shortName="AUD 275" longName="Intraoperative Monitoring" units="3" url="/AUDL#AUD275">
	<p>Methods and procedures required for monitoring of evoked auditory responses in the operating room. The indications for the use of intraoperative monitoring and difficulties unique to this setting. The course will include the opportunity to observe auditory potential monitoring in the operating room.</p>

	<p>Prerequisites: second- or third-year AuD student or consent of instructor.</p>
</CourseCard>
<CourseCard id="AUD276" shortName="AUD 276" longName="Cochlear Implants and Other Implantable Sensory Aids" units="3" url="/AUDL#AUD276">
	<p>Theory and practice of cochlear implantation with focus on audiological management of patients after implantation. Current concepts regarding the activation of primary auditory neurons by electrical stimulation and indications for cochlear implantation and surgical procedures are described.</p>

	<p>Prerequisites: second- or third-year AuD student or consent of instructor.</p>
</CourseCard>
<CourseCard id="AUD278" shortName="AUD 278" longName="Special Topics in Audiology" units="3" url="/AUDL#AUD278">
	<p>Current and developing topics in audiology introduced in group projects, presentations, and mock clinical scenarios. Improving grasp of audiology underpinnings, business practices, critiquing treatments, and emerging technologies.</p>

	<p>Prerequisites: second- or third-year AuD student or consent of instructor.</p>
</CourseCard>
<CourseCard id="AUD284" shortName="AUD 284" longName="Clinical Practice in Audiology II" units="1–4" url="/AUDL#AUD284">
	<p>Applications of clinical procedures to patient assessment. Includes clinical observation, interaction with otologists, and supervised patient care involving diagnostics and hearing-aid evaluations in the UC San Diego audiology clinics. This course may be taken more than once.</p>

	<p>Prerequisites: open to second- or third-year doctoral students or consent of instructor. One quarter unit represents three to four hours per week. Must also be concurrently enrolled in <CourseLink id={327} href="/AUDL#AUD291">AUD 291</CourseLink>.</p>
</CourseCard>
<CourseCard id="AUD291" shortName="AUD 291" longName="Clinical Case Studies/Staffing" units="1" url="/AUDL#AUD291">
	<p>Presentations and discussion of clinical cases and issues related to clinical practice. Students’ clinical experiences are discussed relative to medical and audiological assessment and management. This course may be taken more than once.</p>

	<p>Prerequisites: second- or third-year AuD student or consent of instructor. Must be taken more than once.</p>
</CourseCard>
<CourseCard id="AUD296" shortName="AUD 296" longName="Research Practicum" units="3" url="/AUDL#AUD296">
	<p>Participation in specific faculty research activities. Become familiar with research methods and objectives of a faculty’s research project. Participate in data collection and analysis at the discretion of the faculty investigator.</p>

	<p>Prerequisites: second- or third-year AuD student and consent of instructor.</p>
</CourseCard>
<CourseCard id="AUD298" shortName="AUD 298" longName="Independent Research" units="3" url="/AUDL#AUD298">
	<p>Independent research on topics relevant to audiology, consisting of literature review, data collection. Faculty supervision and mentoring on practical elements of research design and methodology. The course will consist of laboratory experience, focused on practical elements of research and design and methodology.</p>

	<p>Prerequisites: consent of AuD program adviser and consent of faculty mentor.</p>
</CourseCard>
<CourseCard id="AUD299" shortName="AUD 299" longName="Doctoral Project" units="3" url="/AUDL#AUD299">
	<p>Individual investigation and preparation of the doctoral project for the AuD degree will be performed under the supervision of an experienced research mentor.</p>

	<p>Prerequisites: advancement to candidacy in the AuD program.</p>
</CourseCard>
