<h2 id="upper-division">Upper Division</h2>
<CourseCard id="TWS132" shortName="GSS 132" longName="Literature and Global South Societies" units="4" url="/GSS#TWS132">
	<p>This course will investigate novelistic and dramatic treatments of European society in the era of nineteenth-century imperialism, Global South societies under the impact of colonialism, and the position of national minorities inside the United States to the present day. Attention will center on the interplay between the aesthetic merits and social-historical-philosophical content of the works read.</p>

</CourseCard>
<CourseCard id="TWS190" shortName="GSS 190" longName="Undergraduate Seminars" units="4" url="/GSS#TWS190">
	<p>Seminars will be organized on the basis of topics with readings, discussions, and papers. Specific subjects to be covered will change each quarter depending on particular interest of instructors or students. May be repeated for credit.</p>

</CourseCard>
<CourseCard id="TWS197" shortName="GSS 197" longName="Fieldwork" units="4" url="/GSS#TWS197">
	<p>In an attempt to explore and study some unique processes and aspects of community life, students will engage in research in field settings. Topics to be researched may vary, but in each case the course will provide skills for carrying out these studies.</p>

</CourseCard>
<CourseCard id="TWS198" shortName="GSS 198" longName="Directed Group Studies" units="2 or 4" url="/GSS#TWS198">
	<p>Directed group study on a topic or in a field not included in the regular department curriculum, by special arrangement with a faculty member.</p>

	<p>Prerequisites: upper-division standing.</p>
</CourseCard>
<CourseCard id="TWS199" shortName="GSS 199" longName="Independent Study" units="2 or 4" url="/GSS#TWS199">
	<p>Tutorial, individual guided reading and research projects (to be arranged between student and instructor) in an area not normally covered in courses currently being offered in the department. (P/NP grades only.)</p>

	<p>Prerequisites: upper-division standing and consent of instructor. (F,W,S)</p>
</CourseCard>
